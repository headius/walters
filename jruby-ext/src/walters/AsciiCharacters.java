package walters;

final class AsciiCharacters {
    private AsciiCharacters() {}
    
    static final byte ZERO = 0x30;
    static final byte NINE = 0x39;
    static final byte LOWER_A = 0x61;
    static final byte UPPER_A = 0x41;
    static final byte LOWER_F = 0x66;
    static final byte UPPER_F = 0x46;
    static final byte LOWER_N = 0x6e;
    static final byte LOWER_X = 0x78;
    static final byte UPPER_X = 0x58;
    static final byte PERCENT = 0x25;
    static final byte QUOTE = 0x22;
    static final byte AMPER = 0x26;
    static final byte APOS = 0x27;
    static final byte SLASH = 0x2f;
    static final byte BACKSLASH = 0x5c;
    static final byte LT = 0x3c;
    static final byte CR = 0x0d;
    static final byte NL = 0x0a;
    static final byte PLUS = 0x2b;
    static final byte SPACE = 0x20;
    static final byte HASH = 0x23;
    static final byte QUERY = 0x3f;
    static final byte SEMICOLON = 0x3b;
}
