    private final byte[] safe_table;

    public EscapeURI(byte[] safe_table) {
        this.safe_table = safe_table;
    }
    
    @Override
    public ByteList transform(byte[] src, int off, int size) {
        ByteList ob = null;
        int i = 0, org;

        while (i < size) {
            org = i;
            while (i < size && safe_table[src[off + i] & 0xff] != 0)
                i++;

            if (i >= size && org == 0) {
                return null;
            }

            if (ob == null) ob = new ByteList(HOUDINI_ESCAPED_SIZE(size));
            if (i > org) ob.append(src, off + org, i - org);

		    /* escaping */
            if (i >= size)
                break;

            if (src[off + i] == SPACE && safe_table == URL_SAFE) {
                ob.append(PLUS);
            } else {
                hexEscape(ob, src[off + i]);
            }

            i++;
        }

        return ob;
    }
